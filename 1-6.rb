# N×Nの行列
# 追加の領域あり
array = [[[0, 0], [1, 0], [2, 0], [3, 0]],
         [[0, 1], [1, 1], [2, 1], [3, 1]],
         [[0, 2], [1, 2], [2, 2], [3, 2]],
         [[0, 3], [1, 3], [2, 3], [3, 3]],
        ]

n = 4

new_array = []
0.upto(n-1) do |i|
  (n-1).downto(0) do |j|
    new_array << array[j][i]
  end
end

p new_array
